#include <stdio.h>

int main() {
    int a = 10;
    int b = 5;

    // Opérations
    int somme = a + b;
    int difference = a - b;
    int produit = a * b;
    int division = a / b;

    printf("Somme: %d\n", somme);
    printf("Différence: %d\n", difference);
    printf("Produit: %d\n", produit);
    printf("Division: %d\n", division);

    return 0;
}
